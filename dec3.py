def dec3(map, right_steps, down_steps):
    splitted = map.split("\n")
    del splitted[-1]
    COLUMNS = len(splitted[0])
    ROWS = len(splitted)
    current_row = 0
    current_col = 0
    trees = 0

    def go_right(col):
        return (col + right_steps) % COLUMNS

    def go_down(row):
        return row + down_steps

    def is_tree(row, col):
        if splitted[row][col] == "#":
            return True

    while current_row < (ROWS-1):
        current_col = go_right(current_col)
        current_row = go_down(current_row)
        if is_tree(current_row, current_col):
            trees += 1
    return trees


